    public function getVersions()
    {
        if (isset($this->versions['desktop'])) {
            // array_merge_recursive can't be used here because the stability keys are numeric, which will just end
            // up getting appended as an incremented key
            foreach ($this->versions['desktop'] as $stability => $versions) {
                $windows = array_merge_recursive($this->versions['windows'][$stability], $versions);
                $mac     = array_merge_recursive($this->versions['mac'][$stability], $versions);
                $linux   = array_merge_recursive($this->versions['linux'][$stability], $versions);

                uksort($windows, 'version_compare');
                uksort($mac, 'version_compare');
                uksort($linux, 'version_compare');

                $this->versions['windows'][$stability] = $windows;
                $this->versions['mac'][$stability]     = $mac;
                $this->versions['linux'][$stability]   = $linux;
            }

            unset($this->versions['desktop']);
        }

        return parent::getVersions();
    }
